    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        // TODO add your handling code here:
          String rollNo = jTextField1.getText();
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm", "root", "27432288");
            con.setAutoCommit(false);
                try (Statement st = con.createStatement()) {       
                    int resultRows = st.executeUpdate("DELETE FROM result WHERE rollNo='" + rollNo + "'");
                    int studentRows = st.executeUpdate("DELETE FROM Student WHERE rollNo='" + rollNo + "'");
                    con.commit();       
                    if (resultRows > 0 && studentRows > 0) {
                    JOptionPane.showMessageDialog(null, "Records deleted successfully");
                }else {
                    JOptionPane.showMessageDialog(null, "No records found for the provided roll number");
                    }
        } catch (Exception ex) {
        con.rollback();
        JOptionPane.showMessageDialog(null, "Error deleting records: " + ex.getMessage());
    } finally {
        con.setAutoCommit(true);
    }
} catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Connection error: " + e.getMessage());
}

    }//GEN-LAST:event_jButton13ActionPerformed

